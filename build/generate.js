// ---- Mira prompt content: bundle the verbatim .md payloads into an importable
// ES module (edge Functions can't read .md at runtime). The .md files remain the
// canonical, human-authored source of truth; this is a generated mirror kept in
// sync on every build. JSON.stringify preserves the text byte-for-byte.
(function bundleMiraContent() {
  const dir = path.join(ROOT, "functions", "mira");
  const tmpl = fs.readFileSync(path.join(dir, "prompt-template.md"), "utf8");
  const lex = fs.readFileSync(path.join(dir, "stage-lexicon.md"), "utf8");

  // The 66 Pairings lexicon, built from the canonical data (verbatim from The 66 —
  // Blend Lexicon) exactly like the on-screen /explore/mindsets page, so Mira
  // knows every pairing the same way she knows the 12×5 stage grid. One line per
  // pairing: Voice × Voice → Blend — texture. Sorted for a byte-stable prompt.
  const pairKeys = Object.keys(DATA.BLEND_NAMES).sort();
  const pairingLexicon = pairKeys.map((k) => {
    const [ka, kb] = k.split("|");
    const a = DATA.ARCHETYPES.find((x) => x.key === ka);
    const b = DATA.ARCHETYPES.find((x) => x.key === kb);
    const name = DATA.BLEND_NAMES[k];
    const tx = DATA.BLEND_TEXTURES[k] || "";
    return "* " + a.name + " × " + b.name + " → " + name + (tx ? " — " + tx : "");
  }).join("\n");

  const out =
    "// GENERATED by build/generate.js from prompt-template.md + stage-lexicon.md\n" +
    "// and the canonical pairing data in assets/soulcraft-data.js.\n" +
    "// Do NOT edit by hand — edit the sources and re-run `node build/generate.js`.\n" +
    "export const PROMPT_TEMPLATE = " + JSON.stringify(tmpl) + ";\n" +
    "export const STAGE_LEXICON = " + JSON.stringify(lex) + ";\n" +
    "export const PAIRING_LEXICON = " + JSON.stringify(pairingLexicon) + ";\n";
  fs.writeFileSync(path.join(dir, "_content.js"), out);
  console.log("wrote functions/mira/_content.js (" + out.length + " bytes, " + pairKeys.length + " pairings)");
})();

stampIndex();

console.log("done.");
